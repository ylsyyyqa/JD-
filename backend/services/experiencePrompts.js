/**
 * 构建经历挖掘的用户消息
 * @param {string} rawMaterial - 用户的粗料直投内容
 * @param {string} jobType - 目标岗位类型（可选）
 * @param {string} jdText - 目标 JD 文本（可选）
 * @returns {string}
 */
export function buildMiningUserMessage(rawMaterial, jobType, jdText) {
  let msg = '请根据以下用户已填写的材料，生成 3-5 条引导性问题帮助用户挖掘更多经历。'

  if (jobType) {
    msg += `\n\n目标岗位类型：${jobType}`
  }

  if (jdText) {
    msg += `\n\n## 目标岗位 JD\n${jdText.substring(0, 2000)}`
  }

  msg += `\n\n## 用户已填写的粗料\n${rawMaterial.substring(0, 5000)}`

  if (rawMaterial.length < 100) {
    msg += '\n\n用户填写的内容很少，请生成更开放、更基础的问题，帮助用户从零开始回忆自己的经历。'
  }

  return msg
}
